package com.ahmet.hasan.yakup.esra.legalcase.model.enums;

/**
 * @brief Enumeration of hearing statuses
 * @author Team
 * @date March 2025
 */
public enum HearingStatus {
    /**
     * Hearing is scheduled for a future date
     */
    SCHEDULED,
    
    /**
     * Hearing has been completed
     */
    COMPLETED,
    
    /**
     * Hearing has been postponed to a later date
     */
    POSTPONED,
    
    /**
     * Hearing has been cancelled
     */
    CANCELLED
}
